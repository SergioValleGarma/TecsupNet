
namespace Clase20240408
{
	struct Calculadora
	{
		public double dato1;
		public double dato2;
		public string operador;
		public double resultado;

		public double sumar() {
		return dato1 + dato2;
		}

        public double restar()
        {
            return dato1 + dato2;
        }

        public double multiplicar()
        {
            return dato1 * dato2;
        }

        public double dividir()
        {
            return dato1 / dato2;
        }
    }
}
